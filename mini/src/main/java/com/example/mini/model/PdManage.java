package com.example.mini.model;

public class PdManage {
	private String productNo;
	private int pStock;
	private int pSaleCnt;
	private String mSaleYn;
	public String getProductNo() {
		return productNo;
	}
	public void setProductNo(String productNo) {
		this.productNo = productNo;
	}
	public int getpStock() {
		return pStock;
	}
	public void setpStock(int pStock) {
		this.pStock = pStock;
	}
	public int getpSaleCnt() {
		return pSaleCnt;
	}
	public void setpSaleCnt(int pSaleCnt) {
		this.pSaleCnt = pSaleCnt;
	}
	public String getmSaleYn() {
		return mSaleYn;
	}
	public void setmSaleYn(String mSaleYn) {
		this.mSaleYn = mSaleYn;
	}
}
