package com.example.mini.model;

public class Product {
	private String productNo;
	private String pPrice;
	private String pCdatetime;
	private String pUdatetime;
	private String pName;
	private String pContent;
	private String pKind;
	private String iLike;
	private String catName;
	private String pStock;
	private String pSaleCnt;
	public String getProductNo() {
		return productNo;
	}
	public void setProductNo(String productNo) {
		this.productNo = productNo;
	}
	public String getpPrice() {
		return pPrice;
	}
	public void setpPrice(String pPrice) {
		this.pPrice = pPrice;
	}
	public String getpCdatetime() {
		return pCdatetime;
	}
	public void setpCdatetime(String pCdatetime) {
		this.pCdatetime = pCdatetime;
	}
	public String getpUdatetime() {
		return pUdatetime;
	}
	public void setpUdatetime(String pUdatetime) {
		this.pUdatetime = pUdatetime;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpContent() {
		return pContent;
	}
	public void setpContent(String pContent) {
		this.pContent = pContent;
	}
	public String getpKind() {
		return pKind;
	}
	public void setpKind(String pKind) {
		this.pKind = pKind;
	}
	public String getiLike() {
		return iLike;
	}
	public void setiLike(String iLike) {
		this.iLike = iLike;
	}
	public String getCatName() {
		return catName;
	}
	public void setCatName(String catName) {
		this.catName = catName;
	}
	public String getpStock() {
		return pStock;
	}
	public void setpStock(String pStock) {
		this.pStock = pStock;
	}
	public String getpSaleCnt() {
		return pSaleCnt;
	}
	public void setpSaleCnt(String pSaleCnt) {
		this.pSaleCnt = pSaleCnt;
	}
}
