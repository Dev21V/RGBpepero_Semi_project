package com.example.mini.model;

public class UserImage {
	private String srcimgNo;
	private String userId;
	private String imgSrc;
	private String imgName;
	private String orgName;
	private String imgType;
	private String imgUsetype;
	public String getSrcimgNo() {
		return srcimgNo;
	}
	public void setSrcimgNo(String srcimgNo) {
		this.srcimgNo = srcimgNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getImgSrc() {
		return imgSrc;
	}
	public void setImgSrc(String imgSrc) {
		this.imgSrc = imgSrc;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getImgType() {
		return imgType;
	}
	public void setImgType(String imgType) {
		this.imgType = imgType;
	}
	public String getImgUsetype() {
		return imgUsetype;
	}
	public void setImgUsetype(String imgUsetype) {
		this.imgUsetype = imgUsetype;
	}
	
	
}
